package com.newgen.iforms.user.ao.model.customerfulfillment;

public class PersonalDetails {
	private String gender;
    private String nationality;
    private String prefix;
    private String fullName;
    private String dateOfBirth;
    private String shortName;
    private String noOfDependants;
    private String minorFlag;
    private String maritalStatus;
    private String religion;
    
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getNationality() {
		return nationality;
	}
	public void setNationality(String nationality) {
		this.nationality = nationality;
	}
	public String getPrefix() {
		return prefix;
	}
	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getDateOfBirth() {
		return dateOfBirth;
	}
	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}
	public String getShortName() {
		return shortName;
	}
	public void setShortName(String shortName) {
		this.shortName = shortName;
	}
	public String getNoOfDependants() {
		return noOfDependants;
	}
	public void setNoOfDependants(String noOfDependants) {
		this.noOfDependants = noOfDependants;
	}
	public String getMinorFlag() {
		return minorFlag;
	}
	public void setMinorFlag(String minorFlag) {
		this.minorFlag = minorFlag;
	}
	public String getMaritalStatus() {
		return maritalStatus;
	}
	public void setMaritalStatus(String maritalStatus) {
		this.maritalStatus = maritalStatus;
	}
	public String getReligion() {
		return religion;
	}
	public void setReligion(String religion) {
		this.religion = religion;
	}

}
