package com.newgen.iforms.user.ao.model.customerfulfillment;

public class UaeNational {
	private String expiryDate;
    private String natlId;
    private String nationalId;
    private String issueDate;
    private String citizenshipId;
    
	public String getExpiryDate() {
		return expiryDate;
	}
	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}
	public String getNatlId() {
		return natlId;
	}
	public void setNatlId(String natlId) {
		this.natlId = natlId;
	}
	public String getNationalId() {
		return nationalId;
	}
	public void setNationalId(String nationalId) {
		this.nationalId = nationalId;
	}
	public String getIssueDate() {
		return issueDate;
	}
	public void setIssueDate(String issueDate) {
		this.issueDate = issueDate;
	}
	public String getCitizenshipId() {
		return citizenshipId;
	}
	public void setCitizenshipId(String citizenshipId) {
		this.citizenshipId = citizenshipId;
	}


}
