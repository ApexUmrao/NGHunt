package com.newgen.ws.request;

public class KeyContactsInfo {

	private String Title;
	private String Nationality;
	private String Name;
	private String ControllingInterest;
	private String DOB;
	public String getTitle() {
		return Title;
	}
	public void setTitle(String title) {
		Title = title;
	}
	public String getNationality() {
		return Nationality;
	}
	public void setNationality(String nationality) {
		Nationality = nationality;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getControllingInterest() {
		return ControllingInterest;
	}
	public void setControllingInterest(String controllingInterest) {
		ControllingInterest = controllingInterest;
	}
	public String getDOB() {
		return DOB;
	}
	public void setDOB(String dOB) {
		DOB = dOB;
	}
	
	



	

}
