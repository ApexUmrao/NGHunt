package com.newgen.wbg.ws.crs.request;

public class DocumentDtls {
	private String docIndex;
	private String docName;
	private String docRefNo; 
	private String docCRSRefNo;
	private String docCRSCertDate;
	public String getDocIndex() {
		return docIndex;
	}
	public void setDocIndex(String docIndex) {
		this.docIndex = docIndex;
	}
	public String getDocName() {
		return docName;
	}
	public void setDocName(String docName) {
		this.docName = docName;
	}
	public String getDocRefNo() {
		return docRefNo;
	}
	public void setDocRefNo(String docRefNo) {
		this.docRefNo = docRefNo;
	}
	public String getDocCRSRefNo() {
		return docCRSRefNo;
	}
	public void setDocCRSRefNo(String docCRSRefNo) {
		this.docCRSRefNo = docCRSRefNo;
	}
	public String getDocCRSCertDate() {
		return docCRSCertDate;
	}
	public void setDocCRSCertDate(String docCRSCertDate) {
		this.docCRSCertDate = docCRSCertDate;
	}

}
