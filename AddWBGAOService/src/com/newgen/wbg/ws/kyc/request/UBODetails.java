package com.newgen.wbg.ws.kyc.request;

public class UBODetails {
	private String Name;
	private String Nationality;
	private String Share;
	private String LegalCntry;
	private String IsPep;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getNationality() {
		return Nationality;
	}
	public void setNationality(String nationality) {
		Nationality = nationality;
	}
	public String getShare() {
		return Share;
	}
	public void setShare(String share) {
		Share = share;
	}
	public String getLegalCntry() {
		return LegalCntry;
	}
	public void setLegalCntry(String legalCntry) {
		LegalCntry = legalCntry;
	}
	public String getIsPep() {
		return IsPep;
	}
	public void setIsPep(String isPep) {
		IsPep = isPep;
	}
	

}
